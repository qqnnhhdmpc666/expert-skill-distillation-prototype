def utc_now() -> str:
    return datetime.now(timezone.utc).isoformat()


def api_key_present() -> bool:
    return bool(os.environ.get("OPENAI_API_KEY"))


def read_json(path: Path, default: Any = None) -> Any:
    if not path.exists():
        return default
    return json.loads(path.read_text(encoding="utf-8-sig"))


def safe_failure_rows() -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    for row in source_failure_rows():
        rows.append(
            {
                "source": row.get("source"),
                "case_id": row.get("case_id"),
                "task_family": row.get("task_family"),
                "activated_capability_group": row.get("activated_capability_group"),
                "feedback_type": row.get("feedback_type") or row.get("after_feedback_type"),
                "false_positive_count": row.get("false_positive_count"),
                "artifact_dir": row.get("artifact_dir"),
            }
        )
    return rows


def validation_cases() -> list[dict[str, Any]]:
    source_status = download_sources(RUN_ROOT / "validation_sources")
